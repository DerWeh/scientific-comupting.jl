# ╔═╡ 7ffbe5fe-4514-45ca-8f3e-48ee5df018a8
md"""
## Interoperability

Julia provides many useful utilities to improve data analysis and reduce errors.
Luckily, most packages integrate nicely such we can also readily visualize them.

### Units
For example Julia has a great package for quantities with physical dimensions with no runtime overhead[^3].
It's advisable to let the computer handle your units if you don't want your [Mars Climate Orbiter to crash](https://en.wikipedia.org/wiki/Mars_Climate_Orbiter).

[^3]: As long as you respect type stability.
"""

# ╔═╡ 6703f18e-e8c4-49f4-8a8c-baf6096b6fe0
md"""
Let's calculate the kinetic energy in ``J`` from velocity and math:
"""

# ╔═╡ 089d3276-d530-4598-9ed3-aa6aa9b92487
velocity = range(0u"m/s", 10u"m/s", length=100)

# ╔═╡ 66a09330-a24a-48e0-a237-c67968d70924
masses = [0.1u"kg", 0.5u"kg", 10u"kg"]

# ╔═╡ 79123197-792d-4b7f-a6e0-9767bcb9f299
kinetic_energies = @. uconvert(u"J", 1//2 * masses' * velocity^2)

# ╔═╡ 974f908a-53dc-492f-84f6-b8a80b313352
md"""
Note, that some mistakes are prohibited by the units you use:
"""

# ╔═╡ 00451360-1142-47ca-9d4f-d48a4a8c92fa
wrong_energies =  @. uconvert(u"J", 1//2 * masses' * velocity)

# ╔═╡ 212a5c72-a742-46db-98d6-348b8102a624
md"""
And we can readily plot the results, with no chance of messing up the units:
"""

# ╔═╡ 15bdf230-a09a-4c68-8dd9-291397979017
default(fontfamily="Computer Modern")

# ╔═╡ 905d1ded-dbfc-411d-86a3-7b6e419f26de
["m=$(mi)" for mi in masses]

# ╔═╡ b7adbb5d-c2b2-4834-9e8d-1a25915ee890
kinetic_energies

# ╔═╡ dbdc53c7-f2bc-4e22-b1b0-e4f3790a4626
begin
	gr()
	plot(xlabel="v", ylabel="E_{kin}", unitformat=(l, u)->latexify(l, u, labelformat=:round))
	for idx in eachindex(masses)
		plot!(velocity, @view(kinetic_energies[:, idx]), label="m=$(masses[idx])")
	end
	plot!()
end

# ╔═╡ 09b8b2fc-2867-4498-9302-13a1dbcbe501
velocity_std = 0.1u"m/s"

# ╔═╡ 34ce792e-9f83-491b-9094-a0eb1ed799c2
md"""
### Simple errors

We can also add simple (Gaussian) measurement errors.
For simplicity, we assume a fixed accuracy of $(velocity_std) for the velocity and a relative error of $(mass_rstd) for the mass.
"""

# ╔═╡ a1606349-d5a0-4ea1-954b-2ffbcf07792d
# we downsample the velocity a bit to get nicer plots
velocity_measured = @. @view(velocity[begin:3:end]) ± velocity_std

# ╔═╡ 4bb49099-af36-4379-9356-40afd92b76b2
mass_rstd = 0.05

# ╔═╡ 7340575f-a877-47ce-b2cf-23abc95ca57f
masses_measured = @. masses ± (masses*mass_rstd)

# ╔═╡ b12c6d57-89d9-422f-a198-ce943cbb7d9f
kinetic_energies_uncertain = @. uconvert(u"J", 1//2 * masses_measured' * velocity_measured^2)

# ╔═╡ baf23986-d825-4869-9f78-10a6f937e8fb
begin
	gr()
	plot(xlabel="v", ylabel="E_{kin}", unitformat=(l, u)->latexify(l, u, labelformat=:round))
	for idx in eachindex(masses)
		plot!(velocity_measured, @view(kinetic_energies_uncertain[:, idx]), label="m=$(masses_measured[idx])")
	end
	plot!()
end

# ╔═╡ 8d9aee2d-e598-4c59-ba77-0f00eca98dc4
md"""
Multiple dispatch sometimes feels like magic, doesn't it?
"""

# ╔═╡ 2aaee53a-bc7f-4b55-bf37-c97c2619c829
md"""
### Sampled errors

The above example used [linear error propagation](https://en.wikipedia.org/wiki/Propagation_of_uncertainty#Linear_combinations) (assuming Gaussian errors), but more often than not, we consider nonlinear effects.
That's why it is typically a good idea to brute-force error propagation:
Just use many input samples (from suitable distribution), and calculate the result.
Computers are very good with repeated computation, so we can often check if our assumptions for error propagation were reasonable.

Again, Julia's got our back and provides us with [MonteCarloMeasurements](https://baggepinnen.github.io/MonteCarloMeasurements.jl/stable/) which is a smart way to do such sampling.
"""

# ╔═╡ 2cc26d9e-2e97-4a8b-ada3-2c36b5957d1a
md"""
!!! warning "Please take this example as proof of concept"
    I have not rigorously checked the correct construction of the distributions.
    The examples may contain errors.
	Please report them.
"""

# ╔═╡ 89651e5a-349d-45b3-8db4-e5ef616b56ef
md"""
For simplicity we assume the same normal distribution for velocity.
Masses are non-negative, thus a normal distribution is not appropriate.
We switch to log-normal.
"""

# ╔═╡ d11f6085-7b9e-4415-bfb2-fd1eaa14e1eb
velocity_mc = @. @view(velocity[begin:3:end]) + Particles(Normal(0, 1)) * velocity_std

# ╔═╡ 6424ff27-93ef-4fed-a08b-5fa519b45997
function error_lognormal(m,std)
    γ = 1+std^2/m^2
    μ = log(m/sqrt(γ))
    σ = sqrt(log(γ))

    return LogNormal(μ,σ)
end

# ╔═╡ b458b13c-6e41-4067-8895-0c66794ca24f
masses_mc = [
	Particles(error_lognormal(ustrip(mi), ustrip(mi)*mass_rstd))*unit(mi)
	for mi in masses
]

# ╔═╡ 860f733c-f298-4c9f-a4e8-cbc938928538
kinetic_energies_mc = @. uconvert(u"J", 1//2 * masses_mc' * velocity_mc^2)

# ╔═╡ d8752d76-582c-4590-ace4-dc5cbabf7e63
md"""
Here, we have to be a bit more careful.
`MonteCarloMeasurements` aren't as seamlessly integrated with `Unitful` as `Measurements` are.
We have to strip the units and reattach them.
"""

# ╔═╡ 7cc7235b-94f5-4703-8fe6-0974157560ba
begin
	gr()
	
	plot(
		xlabel=L"$v$ (%$(unit(velocity_mc[begin])))",
		ylabel=L"$E_{kin}$ (%$(unit(kinetic_energies_mc[begin])))",
	)
	for idx in eachindex(masses)
		plot!(ustrip(velocity_mc), ustrip(@view(kinetic_energies_mc[:, idx])), label="m=$(masses_mc[idx])")
	end
	plot!()
end

# ╔═╡ 08b6b6d5-ecb4-4a40-b2d4-0936d803db1c
md"""
We can show the distribution of a simple point:
"""

# ╔═╡ a3c98c2e-05ed-4c5b-8cc5-7dea0fad108e
plot(kinetic_energies_mc[10,2])

