md"""
`Plots` uses the Julia convention: A bang (`!`) indicates mutation of data.
In this case, the last plot is mutated.
(We can also bind a plot to a variable and mutate that specific plot.)
Due to the reactive nature of [Pluto](https://plutojl.org/en/docs/), it is necessary to keep the functions in one cell using the `begin end` expression.

Note the interactivity provided by the `PlotlyJS` backend:
You can zoom the data; a mouse-over provides you the exact coordinates of a point.

Alternatively, we could have also provided everything as arguments to scatter:
"""

# ╔═╡ 3f1bade5-56f5-4225-b21b-db4f2479e07c
scatter(
	x,
	y,
	markershape=:cross,
	legend=false,
	xlabel="x",
	ylabel="y",
	title="Plot as single call",
)

# ╔═╡ 45b0ac99-dacc-422d-b4f6-acc46e2c0efd
md"""
If you want to show multiple data in the same plot,
you can either repeatedly call a `plot!` function or plot all data at once:
"""

# ╔═╡ 7979a702-cb4b-4d51-bc1c-6b833d5ba0c2
begin
	plot()  # create object
	for power in (2, 3)
		plot!(x, x.^power, label="y = x^$(power)")
	end
	xlabel!("x")
	ylabel!("y")
	title!("Multiple functions in one plot")
end

# ╔═╡ 5cef2a99-615e-4517-a4ce-c91d5696a508
