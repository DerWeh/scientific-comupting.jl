md"""
# Plotting

In this notebook, we will explore how to visualize data in Julia.
We focus on the package [Plots.jl](https://docs.juliaplots.org/stable/)[^1].
`Plots` is a *metapackage* providing a uniform interface to many different plotting backends.
While you need to learn different syntax in Python if you want static plots for publications using `matplotlib` or interactive HTML plots using `plotly`,
you can just switch out the backend in `Plots`.

In this notebook, we use the [PlotlyJS](https://docs.juliaplots.org/latest/backends/#[Plotly-/-PlotlyJS](https://github.com/spencerlyon2/PlotlyJS.jl)) backend to get nice interactive plots.[^2]
Feel free to switch it out:

* [GR](https://docs.juliaplots.org/latest/backends/#[GR](https://github.com/jheinen/GR.jl)): the default backend, which is fast and feature rich.
* [PGFPlotsX](https://docs.juliaplots.org/latest/backends/#[PGFPlotsX](https://github.com/KristofferC/PGFPlotsX.jl)): using `PGF/TikZ` to get native plots for ``\LaTeX``.
* [UnicodePlots](https://docs.juliaplots.org/latest/backends/#[UnicodePlots](https://github.com/JuliaPlots/UnicodePlots.jl)):
  Get simple visualizations directly in your terminal (e.g. when sshing into the cluster).

For general advice on visualization consider <https://www.data-to-viz.com/>.
Take a look at the [caveats](https://www.data-to-viz.com/caveats.html) to avoid common errors.

[^1]: Consider also the [official tutorial](https://docs.juliaplots.org/stable/tutorial/).

[^2]: Plotly is very powerful for interactive data exploration.
     However, it is limited in the amount of data it can display.
     Switching from SVG to WebGL as Plotly's backend, you can maybe render up to 100,000 points.
     This is important if you plot statistic summary plots like a histogram.
     Plotly stores your raw data in the plot, and the browser actually renders the data.
"""

